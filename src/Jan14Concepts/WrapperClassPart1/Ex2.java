package arrbasics.src.Jan14Concepts.WrapperClassPart1;

public class Ex2 {
    public static void main(String[] args) {
        System.out.println(Integer.valueOf("100"));
        System.out.println(Integer.max(30 , 50));
        System.out.println(Integer.min(30 , 50));
        System.out.println(Integer.reverse(500));
        System.out.println(Integer.sum(50,30));
        System.out.println(Integer.valueOf(200));

        double d = 10.20;
        System.out.println(Integer.valueOf((int) d));
        System.out.println(String.valueOf(d));

    }
}
